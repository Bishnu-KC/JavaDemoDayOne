package com.company.dayone;

public class DataTypesDemo {
	public static void main (String args[])
	{
		//Example of data types.
		// 1. Primitive data types.
		char ch='B';
		boolean bol=false;
		byte b1= 123;
		short s =1234;
		int i=123456;
		long l=123456789;
		
		float f=3.14f;
		double d=123.234;
		
		//2. Reference data types.
		String s1="Bishnu";
		Object obj="K C";
		
		// Displaying the outputs
		
		System.out.println("Character: "+ ch);
		System.out.println("Boolean: "+ bol);
		System.out.println(b1+"\n"+s+"\n"+i+"\n"+l+"\n"+f+"\n"+d+"\n"+s1+"\n"+obj);
		
	}

}
